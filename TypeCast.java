public class TypeCast {
    public static void main(String arg[])
    {
        int a=5;
        float b=a;
        System.out.println(b);
        
        float c=67.98f;
        int d=(int)c;
        System.out.println(d);
        
        char e='A';
        int f=e;
        System.out.println(f);
        
        int g=74;
        char h=(char)g;
        System.out.println(h);
    }
}